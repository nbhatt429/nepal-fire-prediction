script = '''
import requests
import numpy as np
import pandas as pd
import xgboost as xgb
import matplotlib.pyplot as plt
import matplotlib.patches as mpatches
from datetime import datetime
import time
import os

print("Nepal Forest Fire Daily Prediction")
print("="*50)
print(f"Date: {datetime.now().strftime('%Y-%m-%d %H:%M')}")

# ── File paths — update these to match your Drive ────
MODEL_PATH   = "/content/drive/MyDrive/xgboost_900m_final.json"
TERRAIN_PATH = "/content/drive/MyDrive/MyDrive/nepal_forest_grid_terrain.csv"
NDVI_PATH    = "/content/drive/MyDrive/MyDrive/nepal_ndvi_monthly_update.csv"
GRID_PATH    = "/content/drive/MyDrive/nepal_forest_grid.csv"
OUTPUT_DIR   = "/content/drive/MyDrive"

# ── Load model ────────────────────────────────────────
model = xgb.XGBClassifier()
model.load_model(MODEL_PATH)
print("Model loaded")

# ── Load static data ──────────────────────────────────
terrain = pd.read_csv(TERRAIN_PATH)
grid    = pd.read_csv(GRID_PATH)
ndvi    = pd.read_csv(NDVI_PATH)

terrain = terrain.drop(columns=["latitude","longitude"], errors="ignore")
static_df = terrain.merge(
    grid[["point_id","latitude","longitude"]],
    on="point_id", how="inner"
).merge(
    ndvi[["point_id","ndvi_30to45","ndvi_60to90",
          "ndvi_trend","ndvi_anomaly"]],
    on="point_id", how="left"
)
static_df["ndvi_30to45"]   = static_df["ndvi_30to45"].fillna(0.30)
static_df["ndvi_60to90"]   = static_df["ndvi_60to90"].fillna(0.35)
static_df["ndvi_trend"]    = static_df["ndvi_trend"].fillna(0.05)
static_df["ndvi_anomaly"]  = static_df["ndvi_anomaly"].fillna(0.0)
static_df["dist_water_km"] = 1.0
forest_df = static_df[
    (static_df["is_burnable"]==1) &
    (static_df["latitude"].notna()) &
    (static_df["longitude"].notna())
].reset_index(drop=True)
print(f"Forest pixels: {len(forest_df):,}")

# ── Date info ─────────────────────────────────────────
now     = datetime.now()
month   = now.month
year    = now.year
season  = {12:3,1:3,2:3,3:2,4:2,5:2,
            6:0,7:0,8:0,9:0,10:1,11:1}[month]
burning = 1 if month in [3,4,5] else 0
baselines = {
    1:{"temp":12.0,"soil":0.20}, 2:{"temp":14.0,"soil":0.19},
    3:{"temp":18.5,"soil":0.18}, 4:{"temp":22.3,"soil":0.155},
    5:{"temp":24.1,"soil":0.14}, 6:{"temp":24.0,"soil":0.22},
    7:{"temp":23.5,"soil":0.30}, 8:{"temp":23.5,"soil":0.32},
    9:{"temp":22.0,"soil":0.28},10:{"temp":19.0,"soil":0.20},
   11:{"temp":15.0,"soil":0.18},12:{"temp":12.0,"soil":0.20},
}
baseline = baselines[month]
print(f"Month:{month}  Season:{season}  Burning:{burning}")
print(f"Baseline temp:{baseline['temp']}  soil:{baseline['soil']}")

# ── Weather fetch ─────────────────────────────────────
def get_weather(lat, lon):
    try:
        url = "https://api.open-meteo.com/v1/forecast"
        params = {
            "latitude":  round(float(lat),4),
            "longitude": round(float(lon),4),
            "hourly": ["temperature_2m","precipitation",
                       "surface_pressure","wind_speed_10m",
                       "relative_humidity_2m",
                       "soil_moisture_0_to_1cm"],
            "forecast_days": 7,
            "timezone": "Asia/Kathmandu"
        }
        r = requests.get(url, params=params, timeout=15)
        h = r.json().get("hourly", {})
        def clean(key, default):
            vals  = h.get(key, [])
            valid = [v for v in vals if v is not None]
            if not valid: return [default]*168
            med = float(np.median(valid))
            return [v if v is not None else med for v in vals]
        temp   = np.array(clean("temperature_2m",    baseline["temp"]), dtype=float)
        precip = np.array(clean("precipitation",      0.0), dtype=float)/1000
        press  = np.array(clean("surface_pressure",  85000), dtype=float)
        wind   = np.array(clean("wind_speed_10m",     5.0), dtype=float)
        rh     = np.array(clean("relative_humidity_2m",50.0), dtype=float)
        soil   = np.array(clean("soil_moisture_0_to_1cm",
                                 baseline["soil"]), dtype=float)
        return {
            "temp_C_mean":           float(np.mean(temp)),
            "temp_C_max":            float(np.max(temp)),
            "temp_anomaly":          float(np.mean(temp))-baseline["temp"],
            "precip_total":          float(np.sum(precip)),
            "precip_mean_daily":     float(np.sum(precip))/7,
            "pressure_mean":         float(np.mean(press)),
            "wind_speed_mean":       float(np.mean(wind)),
            "wind_speed_max":        float(np.max(wind)),
            "rh_mean":               float(np.mean(rh)),
            "rh_min":                float(np.min(rh)),
            "soil_moisture_mean":    float(np.mean(soil)),
            "soil_moisture_min":     float(np.min(soil)),
            "soil_moisture_anomaly": float(np.mean(soil))-baseline["soil"],
        }
    except:
        return None

feature_order = [
    "elevation","slope","aspect","is_south_facing",
    "ndvi_30to45","ndvi_60to90","ndvi_trend","ndvi_anomaly",
    "landcover","is_burnable","dist_water_km",
    "temp_C_mean","temp_C_max","temp_anomaly",
    "precip_total","precip_mean_daily","pressure_mean",
    "wind_speed_mean","wind_speed_max",
    "rh_mean","rh_min",
    "soil_moisture_mean","soil_moisture_min","soil_moisture_anomaly",
    "month","year","season_encoded","is_burning_season",
]

# ── Predict ───────────────────────────────────────────
results = []
errors  = 0
total   = len(forest_df)
print(f"Predicting {total} locations...")

for i, (_, row) in enumerate(forest_df.iterrows()):
    weather = get_weather(float(row["latitude"]),
                          float(row["longitude"]))
    if weather is None:
        errors += 1
        continue
    features = {
        "elevation":         float(row.get("elevation",     500)),
        "slope":             float(row.get("slope",            5)),
        "aspect":            float(row.get("aspect",         180)),
        "is_south_facing":   int(row.get("is_south_facing",    1)),
        "ndvi_30to45":       float(row.get("ndvi_30to45",   0.30)),
        "ndvi_60to90":       float(row.get("ndvi_60to90",   0.35)),
        "ndvi_trend":        float(row.get("ndvi_trend",    0.05)),
        "ndvi_anomaly":      float(row.get("ndvi_anomaly",   0.0)),
        "landcover":         float(row.get("landcover",       10)),
        "is_burnable":       int(row.get("is_burnable",        1)),
        "dist_water_km":     1.0,
        "month":             month,
        "year":              year,
        "season_encoded":    season,
        "is_burning_season": burning,
        **weather
    }
    X    = np.array([[features[f] for f in feature_order]])
    prob = float(model.predict_proba(X)[0][1])
    if   prob >= 0.7: risk = "VERY HIGH"
    elif prob >= 0.5: risk = "HIGH"
    elif prob >= 0.3: risk = "MEDIUM"
    else:             risk = "LOW"
    results.append({
        "latitude":    float(row["latitude"]),
        "longitude":   float(row["longitude"]),
        "probability": round(prob,4),
        "risk_level":  risk,
        "temp_C":      round(weather["temp_C_mean"],1),
        "rain_7d_mm":  round(weather["precip_total"]*1000,1),
    })
    if (i+1) % 500 == 0:
        print(f"  {i+1}/{total} processed...")
    time.sleep(0.1)

# ── Save results ──────────────────────────────────────
date_str   = now.strftime("%Y-%m-%d")
results_df = pd.DataFrame(results)
out_csv = os.path.join(OUTPUT_DIR, f"fire_risk_{date_str}.csv")
results_df.to_csv(out_csv, index=False)

# ── Risk summary ──────────────────────────────────────
print(f"\\nFIRE RISK FORECAST — {date_str}")
print(f"Predicted: {len(results_df)}  Errors: {errors}")
print(results_df["risk_level"].value_counts().to_string())

vh = results_df[results_df["risk_level"]=="VERY HIGH"]
if len(vh) > 0:
    print(f"\\nALERT: {len(vh)} VERY HIGH risk locations")
    print(vh.sort_values("probability", ascending=False)
          .head(10)[["latitude","longitude",
                     "probability","temp_C","rain_7d_mm"]]
          .to_string(index=False))
else:
    print("\\nAll clear — no VERY HIGH risk today")

# ── Plot map ──────────────────────────────────────────
fig, ax = plt.subplots(figsize=(14,8))
cmap = {"LOW":"#16A34A","MEDIUM":"#D97706",
        "HIGH":"#EA580C","VERY HIGH":"#DC2626"}
smap = {"LOW":8,"MEDIUM":18,"HIGH":30,"VERY HIGH":50}
for risk, color in cmap.items():
    sub = results_df[results_df["risk_level"]==risk]
    if len(sub) > 0:
        ax.scatter(sub["longitude"], sub["latitude"],
                   c=color, s=smap[risk], alpha=0.75, zorder=3,
                   label=f"{risk} ({len(sub)})")
patches = [mpatches.Patch(color=cmap[r], label=r)
           for r in ["LOW","MEDIUM","HIGH","VERY HIGH"]]
ax.legend(handles=patches, fontsize=11,
          title="Fire Risk", loc="upper right")
ax.set_xlabel("Longitude", fontsize=12)
ax.set_ylabel("Latitude",  fontsize=12)
ax.set_title(
    f"Nepal Forest Fire Risk Forecast — {date_str}\\n"
    f"XGBoost (AUC=0.967) + Open-Meteo 7-day forecast",
    fontsize=12, fontweight="bold"
)
ax.set_xlim(80.0, 88.2)
ax.set_ylim(26.3, 30.5)
ax.grid(True, alpha=0.3)
ax.spines["top"].set_visible(False)
ax.spines["right"].set_visible(False)
plt.tight_layout()
out_map = os.path.join(OUTPUT_DIR, f"fire_risk_map_{date_str}.png")
plt.savefig(out_map, dpi=150, bbox_inches="tight", facecolor="white")
print(f"Map saved: fire_risk_map_{date_str}.png")
print("DONE")
'''

